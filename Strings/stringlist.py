variable = "This is a string variable"

# We will see how to access string's character using a list
# Every string variable act as a list default
# list is like an array where we can give positions to access characters
# 0 is default and last is n-1 index

print(f"List access of string is '{variable[:]}' ") 

#len() is a general purpose function that can be used on any list object
print(f' The length of the string is {len(variable)}')