# We will dive deep into quotes
# Strings can either use single quotes '' or double quotes ""
# triple quotes is used for multiline string
# if we want to use ' in a string then we will enclose the string in ""
# if we want to use "" in a string then we will enclose the string in ''

single = "Python's is not compiled language"
double = 'Python "Version 3" is popular'
multi = '''Anyway, Python is user friendly
It has a learning curve but still good for beginners
'''

print( single + "\n" + double + "\n" + multi)

